  event.preventDefault();

  // Dapatkan nilai dari setiap input
  const name = document.getElementById('name').value;
  const email = document.getElementById('email').value;
  const subject = document.getElementById('subject').value;
  const message = document.getElementById('message').value;

  // Nomor telepon tujuan (ganti dengan nomor Anda, diawali dengan kode negara)
  const phoneNumber = '6281385850096'; // Contoh: +62 untuk Indonesia, hapus tanda '+'

  // Buat pesan dengan format yang lebih rapi menggunakan template literal
  const whatsappMessage = `--- Pesan Baru dari Website Madu ---\n\n👤 Nama: ${name}\n\n📧 Email: ${email}\n\n🔖 Subjek: ${subject}\n\n📝 Pesan:\n${message}\n\n--- Akhir Pesan ---`;

  // Buat URL WhatsApp dengan pesan yang sudah diformat
  const whatsappUrl = `https://wa.me/${phoneNumber}?text=${encodeURIComponent(whatsappMessage)}`;

